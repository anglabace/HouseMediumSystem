package pojo;

/**
 * 房屋属性表
 * @author admin
 *
 */
public class HouseAttribute {
	
	//房源属性表id
	private int houseAttribute_id;
	//产权年限
	private int propertyYear;
	//建造年代
	private int buildYear;
	//房本年限
	private int houseYear;
	//房本备件
	private String houseFile;
	//建筑结构
	private int houseBuild_id;
	//产权性质
	private int houseNature_id;
	//交易权属
	private int houseOwnerShip_id;
	public int getHouseAttribute_id() {
		return houseAttribute_id;
	}
	public void setHouseAttribute_id(int houseAttribute_id) {
		this.houseAttribute_id = houseAttribute_id;
	}
	public int getPropertyYear() {
		return propertyYear;
	}
	public void setPropertyYear(int propertyYear) {
		this.propertyYear = propertyYear;
	}
	public int getBuildYear() {
		return buildYear;
	}
	public void setBuildYear(int buildYear) {
		this.buildYear = buildYear;
	}
	public int getHouseYear() {
		return houseYear;
	}
	public void setHouseYear(int houseYear) {
		this.houseYear = houseYear;
	}
	public String getHouseFile() {
		return houseFile;
	}
	public void setHouseFile(String houseFile) {
		this.houseFile = houseFile;
	}
	public int getHouseBuild_id() {
		return houseBuild_id;
	}
	public void setHouseBuild_id(int houseBuild_id) {
		this.houseBuild_id = houseBuild_id;
	}
	public int getHouseNature_id() {
		return houseNature_id;
	}
	public void setHouseNature_id(int houseNature_id) {
		this.houseNature_id = houseNature_id;
	}
	public int getHouseOwnerShip_id() {
		return houseOwnerShip_id;
	}
	public void setHouseOwnerShip_id(int houseOwnerShip_id) {
		this.houseOwnerShip_id = houseOwnerShip_id;
	}
	
	
	
	

}
